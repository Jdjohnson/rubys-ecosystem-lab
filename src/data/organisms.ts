// Helper lookups
export function getOrganism(id: string): Organism | undefined {
  return organisms.find(o => o.id === id);
}

export function getAbioticFactor(id: string): AbioticFactor | undefined {
  return abioticFactors.find(a => a.id === id);
}

export function getItem(id: string): Organism | AbioticFactor | undefined {
  return getOrganism(id) || getAbioticFactor(id);
}

export function getOrganismsByHabitat(habitat: string): Organism[] {
  return organisms.filter(o => o.habitats.includes(habitat as never));
}

export function getAbioticByHabitat(habitat: string): AbioticFactor[] {
  return abioticFactors.filter(a => a.habitats.includes(habitat as never));
}
